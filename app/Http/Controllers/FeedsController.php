    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validator = Validator::make($request->all(), [
            'name' => 'required',
            'description' => 'required',
        ]);
        if (!$validator->fails()) {
            $feed = new Feeds();
            $feed->name = $request->name;
            $feed->description = $request->description;
            $feed->save();
            Notification::route('mail', env('MANAGER_EMAIL'))->notify(new \App\Notifications\NewRecordNotification("New Feed", "A new feed has been added by farm manager", "Please Login to the application for details"));
            return response()->json(['message' => 'Feed Added Successfully'], 200);
        } else {
            return $validator->errors();
        }
    }

